    private void botonFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonFinalizarActionPerformed
        try {
            int delay = 1500;
            lblError.setText("");
            int codigo = Integer.parseInt(id_Empleado);
            int codigoGerente = Integer.parseInt(txtIdGerente.getText());

            if (AccesoEmpleado.eliminar(codigo, codigoGerente)) {
                Timer timer = new Timer(delay, new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        dispose();
                        ((Timer) e.getSource()).stop();
                    }
                });
                timer.start();
                lblExito.setText("ACCIÃ“N DE ELIMINAR EXITOSA");
                botonCancelar.setEnabled(false);
                botonFinalizar.setEnabled(false);
            } else {
                lblError.setText("No se ha podido eliminar al empleado");
            }

        } catch (NumberFormatException nfe) {
            lblError.setText(nfe.getMessage());
        } catch (ClassNotFoundException cnfe) {
            lblError.setText("Error al conectar con la base de datos");
        } catch (SQLException sqle) {
            lblError.setText("Error con la base de datos");
        }
    }//GEN-LAST:event_botonFinalizarActionPerformed


